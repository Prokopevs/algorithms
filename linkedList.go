package main

import (
	"fmt"
)

type Node struct {
	data string
	next *Node
}

var head *Node = &Node{data: "LU", next: nil}

func main() {
	var ff *Node = &Node{data: "GW", next: nil}
	head.next = ff

	var tail *Node = &Node{data: "RT", next: nil}
	ff.next = tail

	p := head
	for {
		fmt.Println(p.data)
		if p.next == nil {
			break
		}
		p = p.next
	}
}
